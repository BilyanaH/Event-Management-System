package bg.fmi.eventplatform.vo;

public enum AgendaItemType {
    OPENING_SPEECH,         // main/opening speech
    GROUP_DISCUSSION,       // group discussion with multiple speakers
    WORKSHOP,               // hands-on interactive session
    PRESENTATION,           // regular talk/lecture by a speaker
    NETWORKING,             // free time for attendees to connect
    BREAK,                  // coffee/lunch break
    CHECK_IN,               // check-in time at the start of the event
    PERFORMANCE,            // music, art, or other live performance
    CEREMONY,               // awards, opening, closing ceremonies
    QA,                     // question and answer session
    OTHER
}